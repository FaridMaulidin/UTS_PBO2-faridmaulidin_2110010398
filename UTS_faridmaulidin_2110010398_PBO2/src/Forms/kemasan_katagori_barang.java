    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            
            if (txtcategoryid.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "categoryid belum di isi");
                txtcategoryid.requestFocus();
            }else if (txtcategory_nama.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "category_nama belum di isi");
                txtcategory_nama.requestFocus();
            }else {
                String sql = "insert into package_category value ('"+txtcategoryid.getText()+"','"+txtcategory_nama.getText()+"','"+tstok.getText()+"' )";
                myObject.Konek.createStatement().execute(sql);
                tampilDatapackage_category();
                JOptionPane.showMessageDialog(this, "Data Berhasil Disimpan");
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.toString());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        try {
            if (
                    (txtcategoryid.getText().isEmpty()) &&
                    (txtcategory_nama.getText().isEmpty()) 
                ){
                JOptionPane.showMessageDialog(this, "Silahkan Pilih Data Terlebih Dahulu");
            }else
            {
                String sql = "UPDATE package_category SET category_nama=? WHERE categoryid=?";
                PreparedStatement ubah = myObject.Konek.prepareStatement(sql);
                ubah.setString(1, txtcategory_nama.getText());
                ubah.setString(2, txtcategoryid.getText());
                ubah.executeUpdate();
                JOptionPane.showMessageDialog(this, "Data Berhasil Diubah!");
                tampilDatapackage_category();
            }
        } catch (Exception e){
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed

