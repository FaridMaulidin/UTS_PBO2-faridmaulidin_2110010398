    public void tampilDatapackage_category(){
        try {
            modelTabelpackage_category.getDataVector().removeAllElements();
            modelTabelpackage_category.fireTableDataChanged();
        
            PreparedStatement query = myObject.Konek.prepareStatement("SELECT * FROM package_category");
            ResultSet data = query.executeQuery();
            while (data.next()) {
            Object[] baris = new Object[3];
            baris[0] = data.getString("katagori_id");
            baris[1] = data.getString("nama_katagori");
            baris[2] = data.getString("stok");
            
            modelTabelpackage_category.addRow(baris);
            
            }
            query.close();
            data.close();
            } catch (Exception e){
            }
        
    
    }

